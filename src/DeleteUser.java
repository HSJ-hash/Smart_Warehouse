    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try
      {
          PreparedStatement stmts;
          stmts = con.prepareStatement("select * from user where id=?");
          stmts.setString(1,empid.getText());
          ResultSet rs;
          rs = stmts.executeQuery();
          
          if (rs.next())
          {
              name.setText(rs.getString("username").trim());
              
              
 
              JOptionPane.showMessageDialog(null,"User Found.");
 
          }
          else
          {
              JOptionPane.showMessageDialog(null,"user not found.");
          }
          rs.close();
          
          
      } 
      catch (SQLException ex)
      {
           JOptionPane.showMessageDialog(rootPane,"Error searching data"+ex,
                   "Customer Form",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

