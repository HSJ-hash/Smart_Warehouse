    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int opt = JOptionPane.showConfirmDialog(null,"Are you sure you want to delete?","Delete",JOptionPane.YES_NO_OPTION);
        try
       {
           String id = empid.getText();
           
           

            PreparedStatement stats = con.prepareStatement("Delete from user where id='"+id+"'");
            int rowss =stats.executeUpdate();
            if(rowss>0)
           {
               JOptionPane.showMessageDialog(null,"Deleted..");
               stats.close();
           }
           
            }
        
       catch(   SQLException | HeadlessException ex)
       {
           JOptionPane.showMessageDialog(rootPane, "Error deleting data"+ex,
                   "Customer Form",JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

